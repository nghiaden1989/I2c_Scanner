int main(void)
{
  HAL_Init();
  SystemClock_Config();
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_USART2_UART_Init();
	HAL_Delay(1000);
 	
 	
  while (1)
  {
   I2c_Scan();
	HAL_Delay(2000);
  }
}
void I2c_Scan(void)
{
	for (i=0; i<128; i++)
 	{
		j=i<<1;
 	  result = HAL_I2C_IsDeviceReady(&hi2c1,j, 2, 5);

		deb[i]=result;
 	  if (result == HAL_OK)
 	  {
			debug[debug_counter]=j;
			debug_counter++;
 	  }
 	}
	debug_counter=0;
}



